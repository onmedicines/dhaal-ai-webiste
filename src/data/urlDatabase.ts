// Combine all legitimate URL arrays
const SIMPLIFIED_LEGITIMATE_URLS: SimplifiedURLData[] = [
  ...SHARK_TANK_URLS,
  ...GENERIC_URLS,
  ...RARE_URLS,
];

// Function to convert simplified data to full URLData
const createURLData = (simplified: SimplifiedURLData): URLData => ({
  ...LEGITIMATE_DEFAULTS,
  url: simplified.url,
  domain: simplified.domain,
  confidence: simplified.confidence || LEGITIMATE_DEFAULTS.confidence,
  riskScore: simplified.riskScore || LEGITIMATE_DEFAULTS.riskScore,
  contentQuality:
    simplified.contentQuality || LEGITIMATE_DEFAULTS.contentQuality,
  domainAge: simplified.founded
    ? `${new Date().getFullYear() - parseInt(simplified.founded)} years`
    : "Established",
  registrar: "Verified Registrar",
  location: simplified.location,
  technologies: ["HTTPS", "Mobile App", "Web Platform", "API"],
  legitimacyMarkers: [
    ...LEGITIMATE_DEFAULTS.legitimacyMarkers,
    `${simplified.category} industry leader`,
    ...(simplified.specialMarkers || []),
  ],
});

// Generate the full legitimate URLs array
export const legitimateURLs: URLData[] =
  SIMPLIFIED_LEGITIMATE_URLS.map(createURLData);

// Function to check if a URL is in our legitimate database
export const findLegitimateURL = (inputUrl: string): URLData | null => {
  try {
    const urlObj = new URL(inputUrl.toLowerCase());
    let domain = urlObj.hostname;

    // Remove www. prefix for matching
    if (domain.startsWith("www.")) {
      domain = domain.substring(4);
    }

    // Check exact domain match
    const exactMatch = legitimateURLs.find(
      (urlData) =>
        urlData.domain === domain ||
        urlData.domain === `www.${domain}` ||
        domain === `www.${urlData.domain}`,
    );

    if (exactMatch) {
      return exactMatch;
    }

    // Check if it's a subdomain of a legitimate domain
    const subdomainMatch = legitimateURLs.find((urlData) => {
      const baseDomain = urlData.domain.replace("www.", "");
      return domain.endsWith(`.${baseDomain}`) || domain === baseDomain;
    });

    return subdomainMatch || null;
  } catch (error) {
    console.error("Error analyzing URL:", error);
    return null;
  }
};

